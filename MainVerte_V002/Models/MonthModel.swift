import Foundation

enum Month: String, Codable {
    case january = "Janvier"
    case february = "Février"
    case march = "Mars"
    case april = "Avril"
    case may = "Mai"
    case june = "Juin"
    case july = "Juillet"
    case august = "Août"
    case september = "Septembre"
    case october = "Octobre"
    case november = "Novembre"
    case december = "Decembre"
}
